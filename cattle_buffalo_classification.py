# Function to parse YOLO label files and reorganize dataset
def reorganize_yolo_to_classification(dataset_dir, output_dir='organized_dataset'):
    """
    Convert YOLO format dataset to classification format.
    YOLO format has images and labels in separate folders.
    This function creates a folder structure: output_dir/class_name/image.jpg
    """

    # Paths
    train_images_dir = os.path.join(dataset_dir, 'train', 'images')
    train_labels_dir = os.path.join(dataset_dir, 'train', 'labels')

    # Check if validation/test exists
    valid_images_dir = os.path.join(dataset_dir, 'valid', 'images')
    valid_labels_dir = os.path.join(dataset_dir, 'valid', 'labels')
    test_images_dir = os.path.join(dataset_dir, 'test', 'images')
    test_labels_dir = os.path.join(dataset_dir, 'test', 'labels')

    has_valid = os.path.exists(valid_images_dir)
    has_test = os.path.exists(test_images_dir)

    print(f"Has validation set: {has_valid}")
    print(f"Has test set: {has_test}")

    # Create output directories
    train_output = os.path.join(output_dir, 'train')
    test_output = os.path.join(output_dir, 'test')

    for class_name in class_names:
        os.makedirs(os.path.join(train_output, class_name), exist_ok=True)
        os.makedirs(os.path.join(test_output, class_name), exist_ok=True)

    # Process training images
    print("\nProcessing training images...")
    train_stats = process_split(train_images_dir, train_labels_dir, train_output, class_names)

    # If no separate test/valid set, split from training
    if not has_valid and not has_test:
        print("\nNo validation/test set found. Creating test split from training data...")
        split_train_test(train_output, test_output, test_size=0.2)
    elif has_valid:
        print("\nProcessing validation images as test set...")
        test_stats = process_split(valid_images_dir, valid_labels_dir, test_output, class_names)
    elif has_test:
        print("\nProcessing test images...")
        test_stats = process_split(test_images_dir, test_labels_dir, test_output, class_names)

    print("\n✅ Dataset reorganization complete!")
    return train_output, test_output

def process_split(images_dir, labels_dir, output_dir, class_names):
    """
    Process a single split (train/valid/test)
    """
    stats = Counter()

    # Get all image files
    image_files = [f for f in os.listdir(images_dir) if f.lower().endswith(('.jpg', '.jpeg', '.png'))]

    for img_file in image_files:
        # Get corresponding label file
        label_file = os.path.splitext(img_file)[0] + '.txt'
        label_path = os.path.join(labels_dir, label_file)
        img_path = os.path.join(images_dir, img_file)

        # Read label file to get class
        if os.path.exists(label_path):
            with open(label_path, 'r') as f:
                lines = f.readlines()
                if lines:
                    # YOLO format: class_id x_center y_center width height
                    # We only need the class_id (first value)
                    class_id = int(lines[0].split()[0])
                    class_name = class_names[class_id]

                    # Copy image to appropriate class folder
                    dest_path = os.path.join(output_dir, class_name, img_file)
                    shutil.copy2(img_path, dest_path)

                    stats[class_name] += 1

    print(f"  Processed {sum(stats.values())} images")
    for class_name, count in stats.items():
        print(f"    {class_name}: {count} images")

    return stats

def split_train_test(train_dir, test_dir, test_size=0.2):
    """
    Split training data into train and test sets
    """
    for class_name in os.listdir(train_dir):
        class_train_dir = os.path.join(train_dir, class_name)
        class_test_dir = os.path.join(test_dir, class_name)

        if os.path.isdir(class_train_dir):
            # Get all images
            images = [f for f in os.listdir(class_train_dir) if f.lower().endswith(('.jpg', '.jpeg', '.png'))]

            if images: # Add check for non-empty list of images
                # Split
                train_imgs, test_imgs = train_test_split(images, test_size=test_size, random_state=42)

                # Move test images
                for img in test_imgs:
                    src = os.path.join(class_train_dir, img)
                    dst = os.path.join(class_test_dir, img)
                    shutil.move(src, dst)

                print(f"  {class_name}: {len(train_imgs)} train, {len(test_imgs)} test")
            else:
                print(f"  {class_name}: No images found for splitting, skipping.")

