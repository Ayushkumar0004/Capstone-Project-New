def predict_single_image(image_path, model, class_names):
    """
    Predict the class of a single image.

    Args:
        image_path: Path to the image file
        model: Trained Keras model
        class_names: List of class names

    Returns:
        Predicted class and confidence
    """
    # Load and preprocess image
    img = load_img(image_path, target_size=(IMG_HEIGHT, IMG_WIDTH))
    img_array = img_to_array(img)
    img_array = np.expand_dims(img_array, axis=0)
    img_array = img_array / 255.0

    # Make prediction
    predictions = model.predict(img_array, verbose=0)
    predicted_class_idx = np.argmax(predictions[0])
    confidence = predictions[0][predicted_class_idx] * 100

    # Display results
    plt.figure(figsize=(10, 8))
    plt.imshow(img)
    plt.axis('off')
    plt.title(f'Predicted: {class_names[predicted_class_idx]}\nConfidence: {confidence:.2f}%',
             fontsize=16, fontweight='bold', pad=20)
    plt.tight_layout()
    plt.show()

    # Print detailed predictions
    print(f"\nDetailed predictions for all classes:")
    print("="*50)
    for i, class_name in enumerate(class_names):
        prob = predictions[0][i]*100
        bar = '█' * int(prob / 2)
        print(f"  {class_name:15s}: {prob:6.2f}% {bar}")
    print("="*50)

    return class_names[predicted_class_idx], confidence

